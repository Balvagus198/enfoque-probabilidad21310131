# Definir la probabilidad a priori de dos eventos
probabilidad_a_priori_evento_A = 0.3
probabilidad_a_priori_evento_B = 0.7

# Calcular la probabilidad complementaria a priori
probabilidad_a_priori_complementaria_A = 1 - probabilidad_a_priori_evento_A
probabilidad_a_priori_complementaria_B = 1 - probabilidad_a_priori_evento_B

# Mostrar los resultados
print(f"Probabilidad a priori del evento A: {probabilidad_a_priori_evento_A}")
print(f"Probabilidad a priori del evento B: {probabilidad_a_priori_evento_B}")
print(f"Probabilidad a priori complementaria del evento A: {probabilidad_a_priori_complementaria_A}")
print(f"Probabilidad a priori complementaria del evento B: {probabilidad_a_priori_complementaria_B}")
